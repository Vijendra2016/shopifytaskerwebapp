'use client';

import { useState } from 'react';
import Link from 'next/link';
import { Phone, Video } from 'lucide-react';

export default function FloatingContactButtons() {
  const [openItem, setOpenItem] = useState<'call' | 'location' | null>(null);

  const toggle = (item: 'call' | 'location') => {
    setOpenItem(openItem === item ? null : item);
  };

  return (
<div className="fixed right-1 top-1/2 -translate-y-1/2 z-50 flex flex-col gap-1 pointer-events-auto">

      {/* CALL */}
      <div
        className={`flex items-center justify-end rounded-full bg-[#62f6b5] shadow-lg cursor-pointer overflow-hidden transition-all duration-300
        ${openItem === 'call' ? 'w-full' : 'w-10 ml-auto'}`}
        onClick={() => toggle('call')}
      >
        <div className="w-10 h-10 flex items-center justify-center shrink-0">
          <Phone className="text-black  w-5 h-5" />
        </div>

        {openItem === 'call' && (
          <Link
            href="https://wa.me/+917014716631"
            className="text-black font-light pr-5 whitespace-nowrap"
          >
            Connect Over whatsapp
          </Link>
        )}
      </div>

      {/* LOCATION */}
      <div
        className={`flex items-center justify-end rounded-full bg-[#62f6b5] shadow-lg cursor-pointer overflow-hidden transition-all duration-300
        ${openItem === 'location' ? 'w-full' : 'w-10 ml-auto'}`}
        onClick={() => toggle('location')}
      >
        <div className="w-10 h-10 flex items-center justify-center shrink-0">
          < Video  className="text-black w-5 h-5" />
        </div>

        {openItem === 'location' && (
          <Link
            href="https://calendly.com/commercetasker/discovery-call"
            target="_blank"
            className="text-black font-light pr-5 whitespace-nowrap"
          >
            Book a Discovery Call
          </Link>
        )}
        
      </div>
    </div>
  );
}
